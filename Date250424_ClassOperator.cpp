#include<iostream>
using namespace std;

/*
运算符重载
对已有的运算符重新进行定义，赋予其另一种功能，以适应不同的数据类型
*/

/*
1.加号+
实现两个自定义数据类型相加的运算
*/

/*
2.左移<<
可以输出自定义数据类型
*/

/*
3.递增++
实现自定义数据类型的递增运算
*/

/*
4.赋值=
对自定义类型的属性进行值拷贝
注意：如果类中有属性指向堆区，做赋值操作时也会出现深浅拷贝问题
*/

/*
5.关系== !=
以让两个自定义类型对象进行对比操作
*/

/*
6.函数调用()
由于重载后使用的方式非常像函数的调用，因此称为仿函数
仿函数没有固定写法，非常灵活
*/